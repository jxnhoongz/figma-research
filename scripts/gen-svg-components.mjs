// Converts the real section2 SVG assets into themeable React components.
//
// Why: the kit needs the per-theme accent slot (`#9A41FE`) to recolor live.
// Inlining the SVG as JSX and swapping that one fill for `currentColor` lets a
// parent set `color: var(--theme-accent)` and have every accent follow. The
// semantic colors (locked grey, current gold, CTA orange) are left untouched.
//
// Run: `node scripts/gen-svg-components.mjs`
import { readFileSync, writeFileSync, mkdirSync, readdirSync } from 'node:fs'
import { dirname, join, basename } from 'node:path'
import { fileURLToPath } from 'node:url'

const __dirname = dirname(fileURLToPath(import.meta.url))
const SRC = join(__dirname, '..', 'src', 'assets', 'section2')
const OUT = join(__dirname, '..', 'src', 'components', 'svg', 'generated')

// The single themeable accent fill. Everything else is semantic and frozen.
const ACCENT = /#9A41FE/gi

// SVG attribute -> JSX prop. Covers every hyphenated/colon attr the assets use.
const ATTR_MAP = {
  'fill-opacity': 'fillOpacity',
  'fill-rule': 'fillRule',
  'clip-rule': 'clipRule',
  'stroke-width': 'strokeWidth',
  'stroke-opacity': 'strokeOpacity',
  'stroke-linecap': 'strokeLinecap',
  'stroke-linejoin': 'strokeLinejoin',
  'stroke-dasharray': 'strokeDasharray',
  'stroke-miterlimit': 'strokeMiterlimit',
  'stop-color': 'stopColor',
  'stop-opacity': 'stopOpacity',
  'flood-opacity': 'floodOpacity',
  'flood-color': 'floodColor',
  'clip-path': 'clipPath',
  'color-interpolation-filters': 'colorInterpolationFilters',
  'gradientUnits': 'gradientUnits',
  'gradientTransform': 'gradientTransform',
  'patternUnits': 'patternUnits',
  'patternContentUnits': 'patternContentUnits',
  'maskUnits': 'maskUnits',
  'filterUnits': 'filterUnits',
  'xlink:href': 'xlinkHref',
}

function toPascal(name) {
  return name
    .replace(/\.svg$/, '')
    .split(/[-_]/)
    .map((p) => p.charAt(0).toUpperCase() + p.slice(1))
    .join('')
}

function jsxify(svg, uid) {
  let out = svg.replace(/<\?xml[^>]*\?>/g, '').trim()

  // Accent slot -> currentColor (the only per-theme recolor).
  out = out.replace(ACCENT, 'currentColor')

  // Namespace all ids + url(#id) refs so multiple instances never collide.
  out = out
    .replace(/id="([^"]+)"/g, (_, id) => `id="${uid}_${id}"`)
    .replace(/url\(#([^)]+)\)/g, (_, id) => `url(#${uid}_${id})`)
    .replace(/xlink:href="#([^"]+)"/g, (_, id) => `xlinkHref="#${uid}_${id}"`)
    .replace(/href="#([^"]+)"/g, (_, id) => `href="#${uid}_${id}"`)

  // Hyphenated / namespaced attrs -> camelCase JSX props.
  for (const [from, to] of Object.entries(ATTR_MAP)) {
    out = out.replace(new RegExp(`\\b${from.replace(':', '\\:')}=`, 'g'), `${to}=`)
  }

  // Drop xmlns:xlink (React warns); keep base xmlns.
  out = out.replace(/\s+xmlns:xlink="[^"]*"/g, '')

  // Inline style strings -> JSX style objects (camelCased keys).
  out = out.replace(/style="([^"]*)"/g, (_, css) => {
    const obj = css
      .split(';')
      .map((d) => d.trim())
      .filter(Boolean)
      .map((d) => {
        const [k, ...v] = d.split(':')
        const key = k.trim().replace(/-([a-z])/g, (_, c) => c.toUpperCase())
        return `${key}: '${v.join(':').trim()}'`
      })
      .join(', ')
    return `style={{ ${obj} }}`
  })

  // Forward className/style onto the root <svg> and make it fill its box.
  out = out.replace(
    /<svg /,
    '<svg {...props} ',
  )

  return out
}

function emit(svg, name, uid) {
  const comp = toPascal(name)
  const body = jsxify(svg, uid)
  return `// AUTO-GENERATED by scripts/gen-svg-components.mjs — do not edit by hand.
// Source: ${name}. Accent slot (#9A41FE) -> currentColor for theming.
import type { SVGProps } from 'react'

export function ${comp}(props: SVGProps<SVGSVGElement>) {
  return (
    ${body}
  )
}
`
}

const groups = ['status-icon', 'component-38', 'tab-switch', 'map-pattern']
let count = 0
for (const group of groups) {
  const dir = join(SRC, group)
  const outDir = join(OUT, group)
  mkdirSync(outDir, { recursive: true })
  for (const file of readdirSync(dir)) {
    if (!file.endsWith('.svg')) continue
    const svg = readFileSync(join(dir, file), 'utf8')
    // Stable unique prefix per file (group + name), id-safe.
    const uid = `${group}-${basename(file, '.svg')}`.replace(/[^a-zA-Z0-9]/g, '_')
    const comp = toPascal(file)
    writeFileSync(join(outDir, `${comp}.tsx`), emit(svg, file, uid))
    count++
  }
}
console.log(`Generated ${count} SVG components into ${OUT}`)
